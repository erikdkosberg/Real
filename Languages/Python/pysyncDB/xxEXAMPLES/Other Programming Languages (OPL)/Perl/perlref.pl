# 1.) Arithmetic

+   addition
-   subtraction
*   multiplication
/   division

# 2.) Numeric Comparison

==  equality
!=  inequality
<   less than
>   greater than
<=  less than or equal
>=  greater than or equal

# 3.) String Comparison

eq  equality
ne  inequality
lt  less than
gt  greater than
le  less than or equal
ge  greater than or equal

# 4.) Boolean logic

&&  and
||  or
!   not

# 5.) Miscellaneous

=   assignment
.   string concatenation
x   string multiplication (repeats strings)
..  range operator (creates a list of numbers or strings)

